public class Java04 {
    public static void main(String[] args) {
        //Arithmetic Operation

        //for integer values
        int a = 15, b = 10;

        int c = a/b;
        int r = a%b;

        System.out.println(c);
        System.out.println(r);

        //for float values
        float a1 = 12.5f;
        float b1 = 4.5f;

        float c1 = a1/b1;
        float r1 = a1%b1;

        System.out.println(c1);
        System.out.println(r1);

        //different data types

        byte a2 = 10;
        short b2 = 15;
        int c2 = a2 + b2;

        System.out.println(c2);


        float a3 = 12.5f;
        long b3 = 1200;
        double c3 = a3 + b3;

        System.out.println(c3);

        char a4 = 40;
        int b4 = 30;
        int c4 = a4-b4;

        System.out.println(c4);

    }
}
